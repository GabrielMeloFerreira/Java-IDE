package ExemploFor;

public class ExemploFor {
    public static void main(String[] args) {
        
        for (int i = 0; i < 5; i++) {
            // O loop 'for' inicializa a variável 'i' em 0 e continua executando enquanto 'i' for menor que 5.
            // A cada iteração, 'i' é incrementado em 1.

            System.out.println("Indice " + i);
            // Imprime o valor atual de 'i' a cada iteração.
        }
    }
}
