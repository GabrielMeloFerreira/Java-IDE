package ExemploMetodo;

public class MeuPrograma {
    
    public static void cumprimentar() {
        System.out.println("Olá, Mundo!");
        // Método estático 'cumprimentar()' que imprime "Olá, Mundo!" no console.
        // Como é um método estático, pode ser chamado diretamente pela classe
        // sem a necessidade de criar uma instância de 'MeuPrograma'.
    }
}
